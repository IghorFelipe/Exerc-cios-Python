n1 = int(input('Um valor: '))
n2 = int(input('Outro valor '))
soma = n1 + n2
multi = n1 * n2
sub = n1 - n2
div = n1 / n2
di = n1 // n2
e = n1 ** n2
print('A soma dos números é {}'.format(soma))
print('A multiplicação dos dois números é {}'.format(multi))
print('A subtração dos dois números é {}'.format(sub))
print('A divisão dos dois números é {}'.format(div))
print('A divisão inteira dos dois números é {}'.format(di))
print('A potencia é {}'.format(e))